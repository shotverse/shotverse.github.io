function escapeHtml(value) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/\"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function sanitizePromptText(text) {
  return text
    .replace(/\s{2,}/g, " ")
    .replace(/\s+([.!?])/g, "$1")
    .trim();
}

function highlightCameraPhrases(text) {
  const cameraPhrasePattern = /\b(?:[Tt]he camera|[Aa]s the camera|maintaining a (?:consistent\s+)?(?:close-up|medium|wide|low-angle|high-angle|low angle|high angle|static|stationary)\s*(?:shot|view|perspective)?|(?:[Aa]n?|[Tt]he)\s+(?:close-up|medium|wide|low-angle|high-angle|low angle|high angle|static|stationary|eye-level|side-angle|side angle)\s+(?:shot|view|perspective)|at a (?:low-angle|high-angle|low angle|high angle)|from (?:below|behind)|positioned (?:behind|in front of|at|from)\s+(?:a\s+)?(?:low-angle|high-angle|low angle|high angle))[^,.!?;:]*/g;
  const cutoffPattern = /\s+\b(?:revealing|showing|capturing|emphasizing|highlighting|focusing|as|that|which|while)\b[\s\S]*$/i;
  let result = "";
  let cursor = 0;
  let match;

  while ((match = cameraPhrasePattern.exec(text)) !== null) {
    const rawMatch = match[0];
    const shortenedMatch = rawMatch.replace(cutoffPattern, "").trimEnd();

    if (!shortenedMatch) {
      continue;
    }

    const start = match.index;
    const end = start + shortenedMatch.length;
    result += escapeHtml(text.slice(cursor, start));
    result += `<span class="camera-motion">${escapeHtml(text.slice(start, end))}</span>`;
    cursor = end;
    cameraPhrasePattern.lastIndex = end;
  }

  result += escapeHtml(text.slice(cursor));
  return result;
}

function highlightPromptText(text) {
  const sceneCountPattern = /^This scene contains \d+ shots?\.?$/i;
  const cleanedText = sanitizePromptText(text);
  const sentences = cleanedText.match(/[^.!?]+[.!?]?/g) || [cleanedText];

  return sentences
    .map((sentence) => {
      const trimmed = sentence.trim();
      if (!trimmed) {
        return "";
      }

      const escaped = escapeHtml(trimmed);
      if (sceneCountPattern.test(trimmed)) {
        return `<span>${escaped}</span>`;
      }

      return `<span>${highlightCameraPhrases(trimmed)}</span>`;
    })
    .join(" ");
}

function renderPromptBlock(label, text) {
  return `
    <div class="shotverse-prompt-block">
      <div class="shotverse-prompt-label">${escapeHtml(label)}</div>
      <p class="shotverse-prompt-text">${highlightPromptText(text)}</p>
    </div>
  `;
}

const scenePromptCache = {};

const comparisonTrajectoryScenes = new Set([
  "scene_0018",
  "scene_0114",
  "scene_0118",
  "scene_0128",
  "scene_0143",
  "scene_0155",
]);

function parseScenePromptRaw(rawPrompt) {
  const cleanedRawPrompt = rawPrompt.replace(/^\[scene_[^\]]+\]/, "").trim();
  const globalMatch = cleanedRawPrompt.match(/\[global_prompt\]([\s\S]*?)(?=\[shot_\d+_prompt\]|$)/);
  const shotPrompts = Array.from(
    cleanedRawPrompt.matchAll(/\[shot_(\d+)_prompt\]([\s\S]*?)(?=\[shot_\d+_prompt\]|$)/g)
  ).map((match) => ({
    label: `Shot ${match[1]}`,
    text: match[2].trim(),
  }));

  return {
    globalPrompt: globalMatch ? globalMatch[1].trim() : "",
    shotPrompts,
  };
}

async function getScenePromptData(sceneId) {
  if (!sceneId) {
    return null;
  }

  if (!scenePromptCache[sceneId]) {
    if (typeof scenePromptRawData !== "undefined" && scenePromptRawData[sceneId]) {
      scenePromptCache[sceneId] = Promise.resolve(
        parseScenePromptRaw(scenePromptRawData[sceneId])
      );
    } else {
      scenePromptCache[sceneId] = fetch(`./static/scene_prompts/${sceneId}.txt`)
        .then((response) => {
          if (!response.ok) {
            throw new Error(`Failed to load prompt for ${sceneId}`);
          }
          return response.text();
        })
        .then(parseScenePromptRaw)
        .catch(() => null);
    }
  }

  return scenePromptCache[sceneId];
}

function getTrajectoryImagePath(sceneId, trajectoryImages = null) {
  if (
    trajectoryImages &&
    typeof trajectoryImages === "object" &&
    trajectoryImages.shot0 &&
    trajectoryImages.shot1
  ) {
    return {
      shot0: trajectoryImages.shot0,
      shot1: trajectoryImages.shot1,
      fallback: trajectoryImages.shot0,
    };
  }

  if (!sceneId || !comparisonTrajectoryScenes.has(sceneId)) {
    return "";
  }

  return {
    shot0: `./static/trajectories/${sceneId}/shot_0_global_scale.jpg`,
    shot1: `./static/trajectories/${sceneId}/shot_1_global_scale.jpg`,
    fallback: `./static/trajectories/${sceneId}/all_frames.jpg`,
  };
}

